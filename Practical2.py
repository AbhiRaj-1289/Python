import math
x = 0
print("Table of Sins :")
while (x < 5):
    x += 1
    print("Sin(",str(x),") = " +str(math.sin(x)))
x = 0
print("Table of Cosines :")
while (x < 5):
    x += 1
    print("Cos("+str(x)+") = " +str(math.cos(x)))
x = 0
print("Table of Tangents :")
while (x < 5):
    x += 1
    print("Tan(",str(x),")" +str(math.tan(x)))